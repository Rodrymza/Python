#convierte cualquier objeto iterable en un lista

nombre="Rodrigo"
#se convierte el elemento en una lista directamente con el constructor list
lista_nombre=list(nombre)
print(lista_nombre)

#de esta manera obtenemos numeros del 0 al 90 generdos con la clase range en una lista
lista_numeros=list(range(0,100,10))
