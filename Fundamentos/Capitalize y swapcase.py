"""Capitalize formatea la cadena dejando el primer caracter en mayuscula, mientras que swapcase invierte todos los caracteres entre mayusculas y minusculas"""

cadena="hOla cOMo vA"

print(f"Cadena sin formato {cadena}")
print(f"Cadena con capitalize(): {cadena.capitalize()}")
print(f"Cadena con swapcase(): {cadena.swapcase()}")
